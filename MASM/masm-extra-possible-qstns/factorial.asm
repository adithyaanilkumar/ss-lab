ASSUME DS:DATA,CS:CODE

DATA SEGMENT
MSG1 DB 0AH,0DH,"ENTER THE NUMBER:$"
MSG2 DB 0AH,0DH,"THE FACTORIAL IS:$"
DATA ENDS

CODE SEGMENT
START:
      MOV AX,DATA
      MOV DS,AX

      LEA DX,MSG1
      MOV AH,09H
      INT 21H
      MOV AH,01H
      INT 21H
      SUB AL,30H
      MOV AH,00H
      MOV BX,AX
      XOR CX,CX
      XOR DX,DX
      MOV AX,0001H

L1: CALL FACT
    CMP BX,0001H
    JZ L2
    JMP L1

L2: MOV BX,000AH
    DIV BX
    PUSH DX
    MOV DX,0
    INC CX
    OR AX,AX
    JNZ L2

    LEA DX,MSG2
    MOV AH,09H
    INT 21H

L3: POP DX
    ADD DL,30H
    MOV AH,02H
    INT 21H
    LOOP L3

    MOV AH,4CH
    INT 21H

FACT PROC
  MUL BX
  DEC BX
  RET
FACT ENDP

CODE ENDS
END START