DATA SEGMENT

MSG1 DB 10,13,'ENTER A STRING:$'
STR1 DB 100,0,100 dup('$')
LEN1 DW 0000H

MSG2 DB 10,13,'ENTER ANOTHER STRING:$'
STR2 DB 100,0,100 dup('$')
LEN2 DW 0000H

MSG3 DB 10,13,'CONCATENATED STRING IS: $'
STR3 DB 100 dup(0)

DATA ENDS

DISPLAY MACRO MSG
    MOV AH,09
    LEA DX,MSG
    INT 21H
ENDM 

CODE SEGMENT
ASSUME CS:CODE, DS:DATA

START: 
MOV AX,DATA
MOV DS,AX
MOV ES,AX

DISPLAY MSG1

LEA DX,STR1
MOV AH,0AH
INT 21H

MOV AL,[STR1+1]
MOV BX,OFFSET LEN1      
MOV BYTE PTR [BX],AL

DISPLAY MSG2

LEA DX,STR2
MOV AH,0AH
INT 21H

MOV AL,[STR2+1]
MOV BX,OFFSET LEN2    
MOV BYTE PTR [BX],AL
  
LEA SI,STR1+2
MOV CX,LEN1

LEA DI,STR3

DISPLAY MSG3

STORE1:
MOV AL,[SI]
MOV DL,AL
MOV AH,02H
INT 21H
INC SI
DEC CX
JNZ STORE1

LEA SI,STR2+2
MOV CX,LEN2

STORE2:
MOV AL,[SI]
MOV DL,AL
MOV AH,02H
INT 21H
INC SI
DEC CX
JNZ STORE2

EXIT:
MOV AH,4CH
INT 21H

CODE ENDS
END START