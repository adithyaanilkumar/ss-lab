ASSUME DS:DATA,CS:CODE

DATA SEGMENT
MSG1 DB 0AH,0DH,"ENTER FIRST NUMBER:$"
MSG2 DB 0AH,0DH,"ENTER SECOND NUMBER:$"
MSG3 DB 0AH,0DH,"ENTER THE THIRD NUMBER:$"
MSG4 DB 0AH,0DH,"THE AVERAGE IS:$"
DATA ENDS

CODE SEGMENT
START:

MOV AX,DATA
MOV DS,AX

LEA DX,MSG1;Accept First Number
MOV AH,09H
INT 21H

MOV AH,01H
INT 21H
MOV CH,AL
SUB CH,30H
MOV AH,01H
INT 21H
MOV CL,AL
SUB CL,30H
MOV AL,00H
MOV AL,0AH
MUL CH
ADD CL,AL

LEA DX,MSG2; Accept Second Number
MOV AH,09H
INT 21H

MOV AH,01H
INT 21H
MOV BH,AL
SUB BH,30H
MOV AH,01H
INT 21H
MOV BL,AL
SUB BL,30H
MOV AL,00H
MOV AL,0AH
MUL BH
ADD BL,AL

LEA DX,MSG3; Accept Third Number
MOV AH,09H
INT 21H

MOV AH,01H
INT 21H
MOV BH,AL
SUB BH,30H
MOV AH,01H
INT 21H
MOV CH,AL
SUB CH,30H
MOV AL,00H
MOV AL,0AH
MUL BH
ADD AL,CH

ADD AL,BL ; Add first and second numbers
ADD AL,CL ; Add third number to the above result

MOV BL,03H ; Store divisor
DIV BL ; Divide

MOV BL,0AH
MOV AH,00H ; Clear Remainder
DIV BL
ADD AX,3030H ; Hex to ASCII
MOV CH,AH

LEA DX,MSG4;Display Result
MOV AH,09H
INT 21H

MOV DL,AL
MOV AH,02H; Display MSD
INT 21H

MOV DL,CH
MOV AH,02H; Display LSD
INT 21H

MOV AH,4CH
INT 21H

CODE ENDS
END START