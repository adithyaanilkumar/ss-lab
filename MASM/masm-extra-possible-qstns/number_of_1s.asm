ASSUME DS:DATA,CS:CODE

DATA SEGMENT
MSG1 DB 0AH,0DH,"ENTER THE SEQUENCE:$"
MSG2 DB 0AH,0DH,"NO:OF 1's IN THE WORD:$"
DATA ENDS

CODE SEGMENT
START:
      MOV AX,DATA
      MOV DS,AX
      MOV CL,00H

      LEA DX,MSG1
      MOV AH,09H
      INT 21H

L1: MOV AH,01H
    INT 21H
    CMP AL,0DH
    JZ L2
    SUB AL,30H
    ADD CL,AL
    JMP L1

L2: LEA DX,MSG2
    MOV AH,09H
    INT 21H

    XOR AX,AX
    MOV AL,CL
    XOR CL,CL
    MOV CL,0AH
    DIV CL
    ADD AL,30H
    ADD AH,30H
    XOR BH,BH
    MOV BH,AH

    MOV DL,AL
    MOV AH,02H
    INT 21H

    MOV DL,BH
    MOV AH,02H
    INT 21H

    MOV AH,4CH
    INT 21H

CODE ENDS
END START